    fun fromName(className: String): FaIconType {
        val (style, originalIconName) = className.split(" ").let {
            if (it.count() == 2) {
                (it[0] to it[1])
            } else {
                ("fa-solid" to className)
            }
        }

        return when(style) {
            "fa-solid" -> FaIconMap.solid[originalIconName]
            "fa-regular" -> FaIconMap.regular[originalIconName]
            "fa-brands" -> FaIconMap.brands[originalIconName]
            else -> null
        } ?: X
    }
